    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
           
        int item=listaVacunas.getSelectedIndex();
        TipoVacuna tipoVacuna=null;
                
        switch (item){
            case 0: tipoVacuna = TipoVacuna.SINOVAC;   break;
            case 1: tipoVacuna = TipoVacuna.PFIZER; break;
            case 2: tipoVacuna = TipoVacuna.ASTRAZENECA; break;
        }
       
        java.util.Date date = new java.util.Date();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("YYYY-MM-dd");
        String fechaVencimiento=formatoFecha.format(jdateFechaVencimiento.getDate());
        
        String laboratorio="";
        int dosisDisponibles=0;
        String[] fecha = new String[3];
        
        if(!txtCantidadDosis.getText().trim().equalsIgnoreCase("")){
            dosisDisponibles = Integer.parseInt(txtCantidadDosis.getText());
        }
            if(!txtLaboratorio.getText().trim().equalsIgnoreCase("")){
                laboratorio = txtLaboratorio.getText();
            }
                if(!fechaVencimiento.trim().equalsIgnoreCase("")){
                    Vacuna vacuna = new Vacuna(tipoVacuna, fechaVencimiento, dosisDisponibles, laboratorio);
            try {
                Sistema.getInstance().registrarVacunas(vacuna);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(IngresarVacuna.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(IngresarVacuna.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(IngresarVacuna.class.getName()).log(Level.SEVERE, null, ex);
            }
                    JOptionPane.showMessageDialog(this, "Se guard√≥ exitosamente");              
                }
        txtCantidadDosis.setText("");
        txtLaboratorio.setText("");
        jdateFechaVencimiento.setCalendar(null);
    }//GEN-LAST:event_btnAceptarActionPerformed

